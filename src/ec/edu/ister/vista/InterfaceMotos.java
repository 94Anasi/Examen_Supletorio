    public void buscar(String codigo) {
        DefaultTableModel molde=  new DefaultTableModel();
        String sql;
        Statement ps;
        ResultSet rs;
        
        molde.addColumn("CÓDIGO");
        molde.addColumn("NOMBRE");
        molde.addColumn("CANTIDAD");
        molde.addColumn("DESCRIPCION");
        molde.addColumn("PRECIO");
        molde.addColumn("SERIE");
        molde.addColumn("AÑO");
        tabla.setModel( molde ); 
        if ( "".equals(codigo) ) {    
            sql = "SELECT * FROM motos";
        } else {                       
            sql = "SELECT * FROM motos WHERE codigo='"+codigo+"';";        }
        String[] tupla = new String[7];
        try {
            ps = obConnection.createStatement();
            rs = ps.executeQuery(sql);
            while (rs.next()) {
                tupla[0] = rs.getString(1);                
                tupla[1] = rs.getString(2);
                tupla[2] = rs.getString(3);                
                tupla[3] = rs.getString(4);
                tupla[4] = rs.getString(5);                
                tupla[5] = rs.getString(6);
                tupla[6] = rs.getString(7);
                molde.addRow(tupla);
            }
            tabla.setModel( molde );
        } catch (SQLException ex) {
            ex.toString();
        }
    }

