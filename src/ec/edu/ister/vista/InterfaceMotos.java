    public void filas(){
    if (tabla.getSelectedRow() >= 0) {
            txtCodigo.setText(tabla.getValueAt(tabla.getSelectedRow(), 0).toString());
            txtNombre.setText(tabla.getValueAt(tabla.getSelectedRow(), 1).toString());
            txtCantidad.setText(tabla.getValueAt(tabla.getSelectedRow(), 2).toString());
            txtDescripcion.setText(tabla.getValueAt(tabla.getSelectedRow(), 3).toString());
            txtPrecio.setText(tabla.getValueAt(tabla.getSelectedRow(), 4).toString());
            txtSerie.setText(tabla.getValueAt(tabla.getSelectedRow(), 5).toString());
            txtAno.setText(tabla.getValueAt(tabla.getSelectedRow(), 6).toString());

        } else {
            JOptionPane.showMessageDialog(null, "Debes seleccionar un registro..!");
        }
    }
